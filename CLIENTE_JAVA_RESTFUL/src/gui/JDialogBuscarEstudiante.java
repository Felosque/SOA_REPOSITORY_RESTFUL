    private void cambiarDatosTabla(int pModo){
        DefaultTableModel modelo = (DefaultTableModel)tablaDatos.getModel();
        modelo.getDataVector().removeAllElements();
        revalidate();
        
        estudiantes = (ArrayList<Estudiante>) ServicioLocalEstudiante.darEstudiantesPorNombre(txtFilto.getText());
        
        for (int i = 0; i < estudiantes.size(); i++) {
            Vector fila = new Vector();
            fila.add(estudiantes.get(i).getDocumentoIdentificacion());
            fila.add(estudiantes.get(i).getNombres() + " " + estudiantes.get(i).getApellidos());
            modelo.addRow(fila);
        }
        repaint();
    }

